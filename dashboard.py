        df = pd.read_csv(csv_path)
        
        # Isolate seed 42 for detailed probabilistic metrics
        df_seed = df[df['seed'] == df['seed'].unique()[0]]
        y_true = df_seed['gold_label'].values
        
        # Basic Accuracies

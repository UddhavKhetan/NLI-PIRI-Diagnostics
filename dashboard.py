        with col_stats:
            st.subheader("Probabilistic Diagnostics")
            st.info(f"**Macro F1 (Hyp-Only):** {f1_hyp:.3f}\n\n*Low F1 confirms accuracy drop isn't just a class-imbalance artifact.*")
